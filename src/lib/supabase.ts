import { createClient } from "@supabase/supabase-js";

// Единственное место во всём проекте, где создаётся подключение к базе.
// Все запросы идут через этот объект — как один общий "телефон" до
// Supabase, а не новое соединение в каждом файле.
//
// Оба значения лежат в .env.local (в git не попадает, см. .gitignore).
// Префикс NEXT_PUBLIC_ означает, что Next.js разрешает читать их и в
// браузере тоже. Для этих двух это нормально и так задумано: URL проекта
// и anon-ключ не секретны сами по себе — единственное, что они дают,
// это право постучаться в API. Что реально можно достать, решает RLS
// на стороне базы (у нас — только чтение публичных таблиц).
//
// А вот service_role / secret-ключ — настоящий секрет, он обходит RLS.
// Его сюда класть нельзя никогда: он появится позже, только на сервере,
// без префикса NEXT_PUBLIC_ (Этап 3, шаг 6 — своя админка).
// Клиент создаётся при первом обращении, а не при импорте файла.
// Причина конкретная: проверка ключей на верхнем уровне срабатывала бы
// в момент `import`, то есть у любого, кто просто импортировал что-то из
// projects.ts — включая юнит-тесты чистых функций (rowToProject), которым
// база не нужна вовсе. Тесты падали бы без ключей, и CI пришлось бы
// снабжать секретами ради проверок, не касающихся сети.
//
// Заодно это singleton: клиент создаётся один раз и переиспользуется.
let client: ReturnType<typeof createClient> | null = null;

export function getSupabase() {
  if (client) return client;

  const url = process.env.NEXT_PUBLIC_SUPABASE_URL;
  const anonKey = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY;

  if (!url || !anonKey) {
    // Падаем с понятным текстом. Иначе ошибка вылезла бы где-то в глубине
    // supabase-js невнятным сообщением про undefined.
    throw new Error(
      "Не заданы NEXT_PUBLIC_SUPABASE_URL / NEXT_PUBLIC_SUPABASE_ANON_KEY. " +
        "Создай .env.local в корне проекта (см. .env.example) и перезапусти npm run dev."
    );
  }

  client = createClient(url, anonKey);
  return client;
}
