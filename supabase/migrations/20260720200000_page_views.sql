-- ============================================================
-- page_views — счётчик уникальных посетителей страницы.
--
-- Считаем уникальных за всё время: один человек, сколько бы раз ни
-- открыл страницу, добавляет ровно единицу. Личность посетителя —
-- случайный id из cookie, которую выдаёт сервер.
--
-- Почему path строкой, а не project_id: одна таблица покрывает и
-- каталог (/portfolio), и каждую работу (/portfolio/bluespawn), и
-- будущие товары магазина — без новой таблицы под каждый тип
-- сущности. "Начинай узко, но не закрывай дорогу".
-- ============================================================

create table public.page_views (
  id          uuid primary key default gen_random_uuid(),

  path        text not null,   -- "/portfolio", "/portfolio/bluespawn"
  visitor_id  uuid not null,   -- id из cookie, выдаётся сервером

  -- Хэш от IP + браузера с секретной солью. Сам IP НЕ хранится: он
  -- считается персональными данными, а для нашей задачи (не дать
  -- накрутить счётчик, почистив cookie) хватает необратимого хэша.
  ip_hash     text,

  created_at  timestamptz not null default now()
);

-- Главное в этой таблице. Уникальность "одна страница — один
-- посетитель" обеспечивает БД, а не наш код с предварительной
-- проверкой "а есть ли уже такая строка": проверка-и-вставка двумя
-- запросами может разъехаться, если человек открыл две вкладки разом.
-- Здесь же повторная вставка просто ничего не делает (on conflict).
create unique index page_views_unique_visitor
  on public.page_views (path, visitor_id);

-- Под два частых запроса: "сколько уникальных у этой страницы" и
-- "сколько новых записей с этого ip_hash за последний час" (антинакрутка).
create index page_views_path_idx on public.page_views (path);
create index page_views_ip_recent_idx on public.page_views (ip_hash, created_at);

-- ------------------------------------------------------------
-- Доступ: таблица закрыта для анонима полностью.
--
-- Политик нет вообще — ни на чтение, ни на запись. Это не забывчивость:
--   - пишет сюда только серверный обработчик /api/view под служебным
--     ключом (service_role), а он RLS не подчиняется;
--   - читает счётчики тоже сервер, при сборке страницы.
-- Браузер к этой таблице не обращается никогда, поэтому открывать её
-- анониму незачем. Дать публичный insert было бы прямым приглашением
-- накрутить публично отображаемое число парой строк в консоли.
-- ------------------------------------------------------------
alter table public.page_views enable row level security;

-- ------------------------------------------------------------
-- Готовый агрегат: страница -> количество уникальных.
-- Считать count(*) с группировкой из кода можно, но представление
-- держит эту логику в одном месте и рядом со схемой.
--
-- Читает его тоже сервер под служебным ключом (страницы статические,
-- HTML собирается на сервере), поэтому анониму доступ не нужен —
-- отбираем явно, а не полагаемся на настройки проекта.
-- ------------------------------------------------------------
create view public.page_view_counts as
  select path, count(*)::bigint as views
  from public.page_views
  group by path;

revoke all on public.page_view_counts from anon, authenticated;
revoke all on public.page_views from anon, authenticated;
